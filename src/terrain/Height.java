package terrain;

public class Height {
	private float x;
	private float z;
	private float height;
	public float getX() {
		return x;
	}
	public float getZ() {
		return z;
	}
	public float getHeight() {
		return height;
	}
	public Height(float x, float z, float height) {
		super();
		this.x = x;
		this.z = z;
		this.height = height;
	}
	
}
